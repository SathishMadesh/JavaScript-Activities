// 1 to 10 using for loop

console.log("1 to 10 using for loop")
for(var i=1; i<=10; i=i+1){
    console.log(i);
}

// 1 to 10 using while loop
console.log("1 to 10 using while loop")

let a=1;
while (a<=10) {
    console.log(a);
    a = a+1;
}


//testing Do while loop

console.log("Testing Do while loop")

let b = 1;

do {
    console.log("Printing one in Do part :",b);
    b=b+1;
}
while (b >= 10) {
    console.log(b);
}